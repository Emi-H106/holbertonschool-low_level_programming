#include "3-calc.h"

/**
 * op_add - addition calculation
 * @a: number 1
 * @b: number 2
 * Return: the result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction calculation
 * @a: number 1
 * @b: number 2
 * Return: the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - mutiplication calculation
 * @a: number 1
 * @b: number 2
 * Return: the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division calculation
 * @a: number 1
 * @b: number 2
 * Return: the result
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulus calculation
 * @a: number 1
 * @b: number 2
 * Return: the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}

